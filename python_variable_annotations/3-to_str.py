#!/usr/bin/env python3
"""
Module 3-to_str
Provides a function to convert a float to its string representation
using type annotations.
"""


def to_str(n: float) -> str:
    """
    Converts a float to its string representation.

    Args:
        n (float): The floating-point number to convert.

    Returns:
        str: The string representation of the float.
    """
    return str(n)
