#!/usr/bin/env python3
"""
Module 0-add
Provides a function to add two floating-point numbers with type annotations.
"""


def add(a: float, b: float) -> float:
    """
    Adds two float values and returns their sum.

    Args:
        a (float): First number.
        b (float): Second number.

    Returns:
        float: The sum of a and b.
    """
    return a + b
